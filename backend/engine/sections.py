def get_all_sections(
    section_type: str = None,
    catalog: str = None,
    limit: int = 50
) -> List[Dict[str, Any]]:
    """Obtener todas las secciones, opcionalmente filtradas"""
    all_sections = {**AISC_SECTIONS, **CHILEAN_SECTIONS}
    
    result = []
    for section in all_sections.values():
        if section_type and section.get("type") != section_type:
            continue
        if catalog and section.get("catalog") != catalog.upper():
            continue
        result.append(section)
        if len(result) >= limit:
            break
    
    return result


def get_section_by_id(section_id: str) -> Optional[Dict[str, Any]]:
    """Obtener una sección por su ID"""
    section_id = section_id.upper()
    return AISC_SECTIONS.get(section_id) or CHILEAN_SECTIONS.get(section_id)


def search_sections(query: str, catalog: str = None) -> List[Dict[str, Any]]:
    """Buscar secciones por nombre"""
    query = query.upper()
    all_sections = {**AISC_SECTIONS, **CHILEAN_SECTIONS}
    
    results = []
    for section_id, section in all_sections.items():
        if query in section_id:
            if catalog and section.get("catalog") != catalog.upper():
                continue
            results.append(section)
    
    return results


def get_section_properties(section_id: str, units: str = "kN-m") -> Dict[str, float]:
    """
    Obtener propiedades de sección para cálculo
    
    Args:
        section_id: ID del perfil
        units: Sistema de unidades
    
    Returns:
        Propiedades en unidades consistentes (m, m², m⁴)
    """
    section = get_section_by_id(section_id)
    if not section:
        raise ValueError(f"Sección '{section_id}' no encontrada")
    
    # Convertir de mm a m para unidades kN-m
    # A: mm² -> m²
    # I: mm⁴ -> m⁴
    # S: mm³ -> m³
    
    mm_to_m = 0.001
    mm2_to_m2 = 1e-6
    mm3_to_m3 = 1e-9
    mm4_to_m4 = 1e-12
    
    props = {
        "A": section["A"] * mm2_to_m2,              # m²
        "Ix": section.get("Ix", section.get("I", 0)) * mm4_to_m4,  # m⁴
        "Iy": section.get("Iy", section.get("I", 0)) * mm4_to_m4,  # m⁴
        "Sx": section.get("Sx", section.get("S", 0)) * mm3_to_m3,  # m³
        "Zx": section.get("Zx", section.get("Z", 0)) * mm3_to_m3,  # m³
        "rx": section.get("rx", section.get("r", 0)) * mm_to_m,    # m
        "ry": section.get("ry", section.get("r", 0)) * mm_to_m,    # m
        "d": section.get("d", section.get("OD", section.get("H", 0))) * mm_to_m,  # m
        "weight": section.get("weight", 0),  # kg/m
    }
    
    return props
